    model = (data.get("judge_model")
             or os.environ.get("JUDGE_MODEL", "").strip()
             or DEFAULT_JUDGE_MODEL)

    if not det and not judge_qs:
        print("nothing to grade", file=sys.stderr)
        return 0

    answer = _read_answer()
    if answer is None:
        print("/app/answer.txt missing or empty", file=sys.stderr)
        return 0

    # --- deterministic portion ---
    det_ok = True
    for c in det:
        if not isinstance(c, dict):
            print(f"[det] non-dict constraint {c!r} -> FAIL")
            det_ok = False
            continue
        iid = c.get("instruction_id")
        fn = CONSTRAINTS.get(iid)
