def _read_answer():
    for p in (ANSWER_PRIMARY, ANSWER_LEGACY):
        if p.exists():
            t = p.read_text(errors="replace")
            if t.strip():
                return t
    return None


def _extract_scores(text, n):
    """Pull n scores from the judge output.

    Preferred: a JSON array of 0/1 (or floats) after a SCORES: marker or a bare
    JSON array. Fallback: the first n numbers in [0,1] found in the text.
    """
    # Try a JSON array anywhere in the text (last one wins — most likely the
    # final answer).
    arrs = re.findall(r"\[[^\[\]]*\]", text)
    for raw in reversed(arrs):
        try:
            val = json.loads(raw)
        except Exception:
            continue
        if isinstance(val, list) and val and all(isinstance(x, (int, float)) for x in val):
            return [max(0.0, min(1.0, float(x))) for x in val]
    # Fallback: scan numbers.
    nums = []
    for m in re.finditer(r"\b(0\.\d+|1\.0|0|1)\b", text):
        try:
            nums.append(max(0.0, min(1.0, float(m.group(1)))))
        except ValueError:
            pass
    return nums[:n] if nums else []


def _run_judge(instruction, answer, questions, threshold, model):
    """Return list[bool] (one per question), or None on hard failure."""
    try:
        from litellm import completion
    except ImportError:
        print("litellm unavailable in container", file=sys.stderr)
        return None
    numbered = "\n".join(f"{i+1}. {q}" for i, q in enumerate(questions))
    system_prompt = (
        "You are a strict, impartial evaluator of whether a candidate response "
        "satisfies a list of yes/no requirements. For each requirement, output "
        "1.0 if the response clearly satisfies it and 0.0 if it does not. "
        "Be literal and conservative."
    )
    user_prompt = (
        "TASK / INSTRUCTION GIVEN TO THE WRITER:\n"
        f"{instruction}\n\n"
        "CANDIDATE RESPONSE:\n"
        f"{answer}\n\n"
        "REQUIREMENTS TO CHECK (in order):\n"
        f"{numbered}\n\n"
        "Return ONLY a JSON array of scores (one per requirement, in order), "
        "each 0.0 or 1.0. Example for 3 requirements: [1.0, 0.0, 1.0]\n"
        "On the final line, repeat the JSON array."
    )
    try:
        resp = completion(
            model=model,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt},
            ],
            temperature=0,
            timeout=JUDGE_TIMEOUT_S,
        )
    except Exception as e:
        print(f"judge API error ({model}): {e}", file=sys.stderr)
        return None
    if not resp or not resp.choices:
        print(f"judge ({model}) returned no choices", file=sys.stderr)
        return None
    out = (resp.choices[0].message.content or "").strip()
    print(f"Judge model: {model}")
    print(f"Judge output:\n{out[:2000]}")
    scores = _extract_scores(out, len(questions))
    if len(scores) < len(questions):
        # Could not parse a score for every question -> fail closed on the
        # missing ones.
        print(f"judge returned {len(scores)} scores for {len(questions)} questions; "
              "missing scored as fail", file=sys.stderr)
        scores = scores + [0.0] * (len(questions) - len(scores))
    return [s >= threshold for s in scores[:len(questions)]]


