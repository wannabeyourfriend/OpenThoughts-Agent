@constraint("detectable_format:sentence_endings")
def _sentence_endings(text, kw):
    # Response must use at least `min_variants` distinct sentence-ending marks.
    min_variants = _as_int(kw.get("min_variants"))
    if min_variants is None:
        return False, "missing min_variants"
    enders = set(re.findall(r"[.!?。！？]", text))
    return len(enders) >= min_variants, f"distinct_enders={sorted(enders)} need>={min_variants}"


